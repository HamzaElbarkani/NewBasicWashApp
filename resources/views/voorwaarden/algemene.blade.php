                <!--Artikel 5-->
                <h2 class="text-xl">Artikel 5 Geschiktheid van het voertuig</h2>
                <p>Het voertuig van Cliënt is alleen geschikt voor de Autowasstraat als het voldoet aan de volgende
                    eisen:</p>
                <ul class="list-disc">
                    <li>Het voertuig heeft vier wielen en een dichte carrosserie; slechts indien uitdrukkelijk door het
                        Autowasbedrijf is aangegeven dat de wasinstallatie tevens geschikt is voor bijvoorbeeld met een
                        soft top of minder dan 4 wielen kan van het voorgaande worden afgeweken.
                    </li>
                    <li>De op de Autowas- en poetsstraat aangegeven maximum-afmetingen (hoogtebreedte-lengte) worden
                        niet overschreden.
                    </li>
                    <li>
