                    doordat zij niet te wijten is aan haar schuld, noch krachtens wet, rechtshandeling of naar in het
                    maatschappelijk verkeer geldende opvattingen voor haar rekening komt, waaronder begrepen het geval
                    dat het Autowasbedrijf door een (toerekenbare) tekortkoming of onzorgvuldigheid van derden niet in
                    staat is haar diensten te verrichten.</p>

