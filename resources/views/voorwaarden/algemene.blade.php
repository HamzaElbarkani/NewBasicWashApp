                <p>Het gebruik van zogenoemde wasboxen van het Autowasbedrijf is geheel voor risico van Cliënt. Het
                    Autowasbedrijf is niet aansprakelijk voor schade aan het voertuig van Cliënt die is ontstaan door
                    het gebruik van de wasbox.</p>
                <!--Artikel 8-->
                <h2 class="text-xl">Artikel 8 Randapparatuur</h2>
                <p>Het gebruik van zogenoemde randapparatuur voor reiniging van de auto van het Autowasbedrijf is geheel
                    voor risico van Cliënt. Het Autowasbedrijf is niet aansprakelijk voor schade aan het voertuig van
                    Cliënt die is ontstaan door het gebruik van de randapparatuur.
                    Onder randapparatuur voor reiniging van de auto wordt onder andere verstaan; Stofzuigers,
                    matten-wassers/reinigers, interieurreinigers, luchtklokken, dashboardguns, verkoopautomaten of
                    vergelijkbare apparatuur. Het Auto- en poetsbedrijf is niet aansprakelijk voor diefstal of verlies
                    van zaken (waaronder lading, inventaris, schriftelijke bescheiden en waardepapieren) van Cliënt
                    en/of derden die zich in het voertuig bevinden. Cliënt is ook zelf verantwoordelijk voor eventueel
                    loszittende delen van het dashboard en andere binnenpanelen van het voertuig.</p>
                <!--Artikel 9-->
                <h2 class="text-xl">Artikel 9 Klachten</h2>
                <p>In geval van een klacht of indien het wasresultaat niet is zoals Cliënt redelijkerwijs mocht
                    verwachten, dient de Cliënt een medewerker van het Autowasbedrijf onverwijld aan te spreken. Wordt
                    de klacht aangaande het wasresultaat door het Autowasbedrijf gegrond verklaard, dan heeft Cliënt
                    recht op éénmaal opnieuw wassen met gebruikmaking van een wasprogramma dat door het Autowasbedrijf
                    wordt gekozen.</p>
