                        medewerker van het Autowasbedrijf om advies te vragen zonder dat deze medewerker de
                        verantwoordelijkheid van de Cliënt overneemt. Cliënt dient bijzonderheden (bv. modificaties of
                        meerdere schaden) voorafgaand aan de was- of poetsbehandeling bij een medewerker van het
                        Autowasbedrijf te melden.
