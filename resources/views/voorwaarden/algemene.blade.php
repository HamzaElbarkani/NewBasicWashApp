                        de verkregen dienst losgerukt kunnen worden. Onderdelen die los kunnen zitten en/of uitsteken
                        zijn o.a. antennes, spiegels, siertrips, stootlijsten, bumpers, spoilers, schijnwerpers en
                        ruiten- en koplampwissers; ook niet originele onderdelen of wijzigingen van de carrosserie
                        kunnen loszitten. Door slijtage, kleine schaden, ouderdom etc. kunnen onderdelen die vast lijken
                        te zitten, toch loszitten.
