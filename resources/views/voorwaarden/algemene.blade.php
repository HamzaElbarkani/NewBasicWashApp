                        betaling(en) direct kunnen worden voldaan. Na uitblijven van reactie en/of betaling wordt er een
                        tweede e-mail naar de Cliënt gestuurd. Cliënt wordt gebeld (of een derde keer gemaild als er
                        geen telefoonnummer bekend is) indien er wederom geen respons is. Mocht reactie en/of betaling
                        nog steeds uitblijven, dan behoudt het Autowasbedrijf het recht om de Cliënt aan te melden bij
                        haar incassopartner.
